package com.example.pojo;

import java.util.Date;

public class EmployeeRecord {
    private String recordId;
    private String employeeName;
    private String gender;
    private String email;
    private String mobile;
    private String address;
    private int age;
    private String educationLevel;
    private String major;
    private int salaryStandard;
    private String bank;
    private String accountNumber;
    private String personalHistory;
    private int createdBy;
    private Date createdDate;
    private String status;
    private int level1Id;
    private int level2Id;
    private int level3Id;
    private int categoryId;
    private int positionId;
    private String photoUrl;

    // Getters and Setters
    public String getRecordId() { return recordId; }
    public void setRecordId(String recordId) { this.recordId = recordId; }
    public String getEmployeeName() { return employeeName; }
    public void setEmployeeName(String employeeName) { this.employeeName = employeeName; }
    public String getGender() { return gender; }
    public void setGender(String gender) { this.gender = gender; }
    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }
    public String getMobile() { return mobile; }
    public void setMobile(String mobile) { this.mobile = mobile; }
    public String getAddress() { return address; }
    public void setAddress(String address) { this.address = address; }
    public int getAge() { return age; }
    public void setAge(int age) { this.age = age; }
    public String getEducationLevel() { return educationLevel; }
    public void setEducationLevel(String educationLevel) { this.educationLevel = educationLevel; }
    public String getMajor() { return major; }
    public void setMajor(String major) { this.major = major; }
    public int getSalaryStandard() { return salaryStandard; }
    public void setSalaryStandard(int salaryStandard) { this.salaryStandard = salaryStandard; }
    public String getBank() { return bank; }
    public void setBank(String bank) { this.bank = bank; }
    public String getAccountNumber() { return accountNumber; }
    public void setAccountNumber(String accountNumber) { this.accountNumber = accountNumber; }
    public String getPersonalHistory() { return personalHistory; }
    public void setPersonalHistory(String personalHistory) { this.personalHistory = personalHistory; }
    public int getCreatedBy() { return createdBy; }
    public void setCreatedBy(int createdBy) { this.createdBy = createdBy; }
    public Date getCreatedDate() { return createdDate; }
    public void setCreatedDate(Date createdDate) { this.createdDate = createdDate; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
    public int getLevel1Id() { return level1Id; }
    public void setLevel1Id(int level1Id) { this.level1Id = level1Id; }
    public int getLevel2Id() { return level2Id; }
    public void setLevel2Id(int level2Id) { this.level2Id = level2Id; }
    public int getLevel3Id() { return level3Id; }
    public void setLevel3Id(int level3Id) { this.level3Id = level3Id; }
    public int getCategoryId() { return categoryId; }
    public void setCategoryId(int categoryId) { this.categoryId = categoryId; }
    public int getPositionId() { return positionId; }
    public void setPositionId(int positionId) { this.positionId = positionId; }
    public String getPhotoUrl() { return photoUrl; }
    public void setPhotoUrl(String photoUrl) { this.photoUrl = photoUrl; }
}
